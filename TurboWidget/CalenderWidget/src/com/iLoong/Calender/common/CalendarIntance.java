package com.iLoong.Calender.common;


public class CalendarIntance
{
	private String begin;
	private String end;
	private String event_id;
	
	public String getBegin()
	{
		return begin;
	}
	
	public void setBegin(
			String begin )
	{
		this.begin = begin;
	}
	
	public String getEnd()
	{
		return end;
	}
	
	public void setEnd(
			String end )
	{
		this.end = end;
	}
	
	public String getEvent_id()
	{
		return event_id;
	}
	
	public void setEvent_id(
			String event_id )
	{
		this.event_id = event_id;
	}
	
}
